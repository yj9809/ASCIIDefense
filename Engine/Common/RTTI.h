// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ(๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ฿ฟ๏ฟฝ) ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝศฏ๏ฟฝ๏ฟฝ ๏ฟฝฯฑ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯด๏ฟฝ ลฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ.
// RunTime-Type-Information.
namespace Wanted
{
	// ฤฟ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ RTTI๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯด๏ฟฝ ลฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝึป๏ฟฝ๏ฟฝ๏ฟฝ ลฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ.
	// C#๏ฟฝ๏ฟฝ Object, Java๏ฟฝ๏ฟฝ object, 
	// ๏ฟฝ๐ธฎพ๏ฟฝ๏ฟฝ๏ฟฝ UObject๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝุบ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ.
	class WANTED_API RTTI
	{
	public:
		virtual const size_t& GetType() const = 0;

		virtual bool Is(RTTI* const rtti) const
		{
			return false;
		}

		virtual bool Is(const size_t id) const
		{
			return false;
		}

		template<typename T>
		bool IsTypeOf()
		{
			return Is(T::TypeIdClass());
		}

		template<typename T>
		T* As()
		{
			if (Is(T::TypeIdClass()))
			{
				return (T*)this;
			}

			return nullptr;
		}

		template<typename T>
		const T* As() const
		{
			if (Is(T::TypeIdClass()))
			{
				return (T*)this;
			}

			return nullptr;
		}
	};
}

// RTTI๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ลฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ฿ฐ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝลฉ๏ฟฝ๏ฟฝ.
// ๏ฟฝฦท๏ฟฝ ๏ฟฝฺตๅฟก๏ฟฝ๏ฟฝ Type, ParentType๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ลธ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝศฏ๏ฟฝวพ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ/๏ฟฝูฟ๏ฟฝ๏ฟฝึฑ๏ฟฝ ๏ฟฝ๏ฟฝ.
#define RTTI_DECLARATIONS(Type, ParentType)												\
friend class RTTI;																		\
protected:																				\
	static const size_t TypeIdClass()													\
	{																					\
		static int runTimeTypeId = 0;													\
		return reinterpret_cast<size_t>(&runTimeTypeId);								\
	}																					\
public:																					\
	virtual const size_t& GetType() const override { return Type::TypeIdClass(); }		\
	using super = ParentType;															\
	virtual bool Is(const size_t id) const override										\
	{																					\
		if (id == TypeIdClass())														\
		{																				\
			return true;																\
		}																				\
		else																			\
		{																				\
			return ParentType::Is(id);													\
		}																				\
	}																					\
	virtual bool Is(RTTI* const rtti) const override									\
	{																					\
		return Is(rtti->GetType());														\
	}